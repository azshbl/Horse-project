def register_trainee():

    trainee_id = max(trainees.keys()) + 1

    print("\n===== TRAINEE REGISTRATION =====")

    name = input("Enter your name: ")
    age = int(input("Enter your age: "))

    while True:

        training_type = input(
            "Choose training type (Jumping / Racing / For fun): "
        ).title()

        if training_type in ["Jumping", "Racing", "For Fun"]:
            break

        print("Invalid training type.")

    while True:

        package = int(input(
            "\nChoose package:\n"
            "10 Classes = 1500 SAR\n"
            "20 Classes = 2900 SAR\n"
            "40 Classes = 5400 SAR\n"
            "Enter package: "
        ))

        if package in [10, 20, 40]:
            break

        print("Invalid package.")

    print("\n===== INITIAL ASSESSMENT =====")
    print("1. Beginner")
    print("2. Intermediate")
    print("3. Advanced")

    while True:

        assessment = input("Enter assessment result: ")

        if assessment == "1":
            level = "Beginner"
            break

        elif assessment == "2":
            level = "Intermediate"
            break

        elif assessment == "3":
            level = "Advanced"
            break

        else:
            print("Invalid choice.")

    trainer_name = assign_trainer(training_type, level)
    horse_name = assign_horse(level)

    trainees[trainee_id] = {

        "trainee_name": name,
        "age": age,
        "training_type": training_type,
        "package": package,
        "level": level,
        "completed_classes": 0,
        "assigned_trainer": trainer_name,
        "assigned_horse": horse_name,
        "license": False
    }

    print("\nRegistration Successful.")
    print("Application Accepted.")
    print(f"Assigned Trainer: {trainer_name}")
    print(f"Assigned Horse: {horse_name}")


def assign_trainer(training_type, level):

    for trainer in trainers.values():

        score = trainer["performance_score"]

        if level == "Beginner" and score < 80:

            if training_type.lower() in trainer["training_type"].lower():
                return trainer["trainer_name"]

        elif level == "Intermediate" and 80 <= score <= 89:

            if training_type.lower() in trainer["training_type"].lower():
                return trainer["trainer_name"]

        elif level == "Advanced" and score >= 90:

            if training_type.lower() in trainer["training_type"].lower():
                return trainer["trainer_name"]

    return "No Trainer Available"



def assign_horse(level):

    for horse in horses.values():

        horse_level = horse["horse_level"]

        if level == "Beginner" and 60 <= horse_level <= 79:
            return horse["horse_name"]

        elif level == "Intermediate" and 80 <= horse_level <= 89:
            return horse["horse_name"]

        elif level == "Advanced" and horse_level >= 90:
            return horse["horse_name"]

    return "No Horse Available"



def show_trainee_info(name):

    for trainee in trainees.values():

        if trainee["trainee_name"].lower() == name.lower():

            print("\n===== MY INFORMATION =====")

            print(f"Name: {trainee['trainee_name']}")
            print(f"Age: {trainee['age']}")
            print(f"Training Type: {trainee['training_type']}")
            print(f"Level: {trainee['level']}")
            print(f"Package: {trainee['package']} Classes")
            print(f"Completed Classes: {trainee['completed_classes']}")
            print(f"Assigned Trainer: {trainee['assigned_trainer']}")
            print(f"Assigned Horse: {trainee['assigned_horse']}")

            if trainee["license"]:
                print("License Status: Licensed")

            else:
                print("License Status: Not Licensed")

            break




def show_progress(name):

    for trainee in trainees.values():

        if trainee["trainee_name"].lower() == name.lower():

            progress = (
                trainee["completed_classes"]
                / trainee["package"]
            ) * 100

            remaining = (
                trainee["package"]
                - trainee["completed_classes"]
            )

            if remaining < 0:
                remaining = 0

            print("\n===== PROGRESS REPORT =====")
            print(f"Completed Classes: {trainee['completed_classes']}")
            print(f"Remaining Classes: {remaining}")
            print(f"Progress: {progress:.0f}%")

            break



def complete_class(name):

    for trainee in trainees.values():

        if trainee["trainee_name"].lower() == name.lower():

            trainee["completed_classes"] += 1

            print("\nClass Completed Successfully.")

            if trainee["completed_classes"] >= 40:

                trainee["license"] = True

                print("""
=========================================
Congratulations!
You have earned your riding license.
=========================================
""")

            break



