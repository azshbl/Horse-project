def horse_statistics():

    total_horses = len(horses)

    stable_horses_count = 0
    boarding_count = 0
    arabian_count = 0
    frezian_count = 0

    for horse in horses.values():

        if horse["status"] == "stable":
            stable_horses_count += 1

        elif horse["status"] == "boarding":
            boarding_count += 1

        if horse["breed"] == "arabian":
            arabian_count += 1

        elif horse["breed"] == "frezian":
            frezian_count += 1

    print("\n===== Horse Statistics =====")
    print(f"Total Horses: {total_horses}")
    print(f"Stable Horses: {stable_horses_count}")
    print(f"Boarding Horses: {boarding_count}")
    print(f"Arabian Horses: {arabian_count}")
    print(f"frezian Horses: {frezian_count}")




