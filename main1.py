# ================= TRAINEE FUNCTIONS =================

def assign_trainer(training_type, level):
    for trainer in trainers.values():
        score = trainer["performance_score"]

        if level == "Beginner" and score < 80:
            if training_type.lower() in trainer["training_type"].lower():
                return trainer["trainer_name"]

        elif level == "Intermediate" and 80 <= score <= 89:
            if training_type.lower() in trainer["training_type"].lower():
                return trainer["trainer_name"]

        elif level == "Advanced" and score >= 90:
            if training_type.lower() in trainer["training_type"].lower():
                return trainer["trainer_name"]

    return "No Trainer Available"


def assign_horse(level):
    for horse in horses.values():
        horse_level = horse["horse_level"]

        if level == "Beginner" and 60 <= horse_level <= 79:
            return horse["horse_name"]

        elif level == "Intermediate" and 80 <= horse_level <= 89:
            return horse["horse_name"]

        elif level == "Advanced" and horse_level >= 90:
            return horse["horse_name"]

    return "No Horse Available"


def register_trainee():
    trainee_id = max(trainees.keys()) + 1

    print("\n===== TRAINEE REGISTRATION =====")

    name = input("Enter your name: ")
    age = int(input("Enter your age: "))

    while True:
        training_type = input("Choose training type (Jumping / Racing / For fun): ")

        if training_type.lower() == "jumping":
            training_type = "Jumping"
            break
        elif training_type.lower() == "racing":
            training_type = "Racing"
            break
        elif training_type.lower() == "for fun":
            training_type = "For fun"
            break
        else:
            print("Invalid training type.")

    while True:
        package = int(input(
            "\nChoose package:\n"
            "10 Classes = 1500 SAR\n"
            "20 Classes = 2900 SAR\n"
            "40 Classes = 5400 SAR\n"
            "Enter package: "
        ))

        if package == 10 or package == 20 or package == 40:
            break
        else:
            print("Invalid package.")

    print("\n===== INITIAL ASSESSMENT =====")
    print("1. Beginner")
    print("2. Intermediate")
    print("3. Advanced")

    while True:
        assessment = input("Enter assessment result: ")

        if assessment == "1":
            level = "Beginner"
            break
        elif assessment == "2":
            level = "Intermediate"
            break
        elif assessment == "3":
            level = "Advanced"
            break
        else:
            print("Invalid choice.")

    trainer_name = assign_trainer(training_type, level)
    horse_name = assign_horse(level)

    trainees[trainee_id] = {
        "trainee_name": name,
        "age": age,
        "training_type": training_type,
        "level": level,
        "completed_classes": 0,
        "package": package,
        "assigned_trainer": trainer_name,
        "assigned_horse": horse_name,
        "license": False
    }

    print("\nRegistration Successful.")
    print("Application Accepted.")
    print(f"Assigned Trainer: {trainer_name}")
    print(f"Assigned Horse: {horse_name}")


